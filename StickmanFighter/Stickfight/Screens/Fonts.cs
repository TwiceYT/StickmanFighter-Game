        private static bool[] B(params string[] rows)
        {
            var result = new bool[35];
            for (int r = 0; r < Math.Min(rows.Length, 7); r++)
                for (int c = 0; c < Math.Min(rows[r].Length, 5); c++)
                    result[r * 5 + c] = rows[r][c] == '1';
            return result;
        }
